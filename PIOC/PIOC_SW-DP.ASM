					BTSC	SFR_DATA_EXCH,3
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					NOP
					BS		SFR_PORT_IO,SB_PORT_OUT1
					NOP
