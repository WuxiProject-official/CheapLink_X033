;
					ORG		0X0000
					DW		0X0000
					JMP		PIOC_INIT
					DW		0X0FFF
; 1#BF=SFR_INDIR_ADDR, 	2#BF=SFR_DATA_EXCH
; 1#FF=SFR_PORT_DIR,	2#FF=SFR_PORT_IO
;
DAP_TURN_CYCLE		EQU		SFR_DATA_REG0
DAP_IDLE_CYCLE		EQU		SFR_DATA_REG1
DAP_DATA_PHASE		EQU		SFR_DATA_REG2
DAP_CMD_TYPE		EQU		SFR_DATA_REG3
DAP_CMD_ARGV		EQU		SFR_DATA_REG4
DAP_CMD_RESP		EQU		SFR_DATA_REG5
DAP_DELAY_CYC_D0	EQU		SFR_DATA_REG6
DAP_DELAY_CYC_D1	EQU		SFR_DATA_REG7
;
DAP_DATA_D0			EQU		SFR_DATA_REG16
DAP_DATA_D1			EQU		SFR_DATA_REG17
DAP_DATA_D2			EQU		SFR_DATA_REG18
DAP_DATA_D3			EQU		SFR_DATA_REG19
DAP_DATA_D4			EQU		SFR_DATA_REG20
DAP_DATA_D5			EQU		SFR_DATA_REG21
DAP_DATA_D6			EQU		SFR_DATA_REG22
DAP_DATA_D7			EQU		SFR_DATA_REG23
;
PIOC_CMD_CODE		EQU		SFR_CTRL_WR
PIOC_CMD_RESP		EQU		SFR_CTRL_RD
;
; outer-CALL used 2cyc, RET used 2cyc, 3n-1cyc for loops
; TAKES: A as loopCnt_n
; BREAKS: A, Z, C
DELAY_L1:			ADDL	0XFF
					JNZ		DELAY_L1
					RET
;
; each DELAY_L1 call takes 3n+3cyc
; outer-CALL used 2cyc, RET used 2cyc, 770D1+3D0+10 for loops
; BREAKS: A, Z, SFR_DATA_REG8
DELAY_L2:			MOV		DAP_DELAY_CYC_D1,A
					CMPZ	0x00,DELAY_L2_2
					MOVA	SFR_DATA_REG8
DELAY_L2_1:			MOVL	0XFF
					CALL	DELAY_L1
					DECSZ	SFR_DATA_REG8,F
					JMP		DELAY_L2_1
DELAY_L2_2:			MOV		DAP_DELAY_CYC_D0,A
					CALL	DELAY_L1
					RET
;
; TAKES: A as loopCnt_n, Z as skipFlag
; BREAKS: A, Z, SFR_DATA_EXCH
DAP_TURN:			MOVA	SFR_DATA_EXCH
					JNZ		DAP_TURN_1
					RET
DAP_TURN_1:			BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					DECSZ	SFR_DATA_EXCH,F
					JMP		DAP_TURN_1
					RET
;
; BREAKS: A, SFR_INDIR_ADDR, SFR_DATA_EXCH
DAP_READ8:			BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,0
					BTSC	SFR_DATA_EXCH,0
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,1
					BTSC	SFR_DATA_EXCH,1
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,2
					BTSC	SFR_DATA_EXCH,2
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,3
					BTSC	SFR_DATA_EXCH,3
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,4
					BTSC	SFR_DATA_EXCH,4
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,5
					BTSC	SFR_DATA_EXCH,5
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,6
					BTSC	SFR_DATA_EXCH,6
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,7
					BTSC	SFR_DATA_EXCH,7
					INC		SFR_INDIR_ADDR,F
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					RET
; TAKES: SFR_DATA_EXCH as data
; BREAKS: A, SFR_INDIR_ADDR
DAP_WRITE8:			BP2F	BO_PORT_OUT0,0
					BTSC	SFR_DATA_EXCH,0
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,1
					BTSC	SFR_DATA_EXCH,1
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,2
					BTSC	SFR_DATA_EXCH,2
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,3
					BTSC	SFR_DATA_EXCH,3
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,4
					BTSC	SFR_DATA_EXCH,4
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,5
					BTSC	SFR_DATA_EXCH,5
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,6
					BTSC	SFR_DATA_EXCH,6
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BP2F	BO_PORT_OUT0,7
					BTSC	SFR_DATA_EXCH,7
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2					
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					RET
;
; BREAKS: A, Z, C, SFR_DATA_EXCH, SFR_INDIR_ADDR
DAP_SWD_TRAN:		CLR		SFR_INDIR_ADDR
; [U8]request in DAP_CMD_ARGV
; [U32]data in DAP_DATA_D0-D3
					MOV		DAP_CMD_ARGV,A
					MOVA	SFR_DATA_EXCH
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
; - PACKET_REQUEST
; - START_BIT	
					BS		SFR_PORT_IO,SB_PORT_OUT0
					BC		SFR_PORT_IO,SB_PORT_OUT1
; PIOC_CMD_CODE[7] means if use fastDelay
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - APnDP_BIT
					BP2F	BO_PORT_OUT0,0
					BTSC	SFR_DATA_EXCH,0
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - RnW_BIT	
					BP2F	BO_PORT_OUT0,1
					BTSC	SFR_DATA_EXCH,1
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - A2_BIT	
					BP2F	BO_PORT_OUT0,2
					BTSC	SFR_DATA_EXCH,2
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - A3_BIT	
					BP2F	BO_PORT_OUT0,3
					BTSC	SFR_DATA_EXCH,3
					INC		SFR_INDIR_ADDR,F
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - PARITY_BIT	
					BP1F	BO_PORT_OUT0,0
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

; - STOP_BIT	
					BC		SFR_PORT_IO,SB_PORT_OUT0
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - PARK_BIT	
					BS		SFR_PORT_IO,SB_PORT_OUT0
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - TURN
					MOVA1F	0B00001110	; IO0(IPU), IO1(OPU)
					MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
; - ACK Response
					CLR		SFR_DATA_EXCH

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,0
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,1
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_CMD_RESP
; - If DAP_OK(0x01)
					CMPL	0X01
					JZ		DAP_SWD_TRAN_OK
; - If DAP_WAIT(0x02)
					CMPL	0x02
					JZ		DAP_SWD_TRAN_NO
; - If DAP_FAULT(0x04)
					CMPL	0x04
					JZ		DAP_SWD_TRAN_NO
; - Else is error, TURN+33.
					MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVL	33
					ANDL	0XFF	; force Z=0
					CALL	DAP_TURN
					BS		SFR_PORT_IO,SB_PORT_OUT0
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
					JMP		PIOC_WAIT_HOST
; - DAP_OK:
DAP_SWD_TRAN_OK:	BTSS	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_OK_W
; - Read data
					CLR		SFR_INDIR_ADDR
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D0
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D1
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D2
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D3
; - Check parity
					CLR		SFR_DATA_EXCH
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,0
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					MOV		SFR_INDIR_ADDR,A
					ANDL	0X01
					XOR		SFR_DATA_EXCH,F
					JZ		DAP_SWD_TRAN_OK_R1
; - Parity mismatch
					MOVL	0X08
					MOVA	DAP_CMD_RESP
DAP_SWD_TRAN_OK_R1:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
					JMP		DAP_SWD_TRAN_OK_TS
; - Write data
DAP_SWD_TRAN_OK_W:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
					CLR		SFR_INDIR_ADDR
					MOV		DAP_DATA_D0,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D1,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D2,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D3,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
; - Send parity
					BP1F	BO_PORT_OUT0,0
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - Capture TS
DAP_SWD_TRAN_OK_TS:	BTSC	DAP_CMD_ARGV,7
					BS		SFR_SYS_CFG,SB_INT_REQ
; - Idle cycles
					MOV		DAP_IDLE_CYCLE,A
					JZ		DAP_SWD_TRAN_OK_E
					BC		SFR_PORT_IO,SB_PORT_OUT0
					CALL	DAP_TURN
DAP_SWD_TRAN_OK_E:	BS		SFR_PORT_IO,SB_PORT_OUT0
					JMP		PIOC_WAIT_HOST
;
; - DAP_WAIT/FAULT:
DAP_SWD_TRAN_NO:	MOV		DAP_DATA_PHASE,F
					JZ		DAP_SWD_TRAN_NO_1
					BTSS	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_NO_1
; * Dummy Read 32+1
					MOVL	33
					ANDL	0XFF	; force Z=0
					CALL	DAP_TURN
DAP_SWD_TRAN_NO_1:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
					MOV		DAP_DATA_PHASE,F
					JZ		DAP_SWD_TRAN_NO_E
					BTSC	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_NO_E
					BC		SFR_PORT_IO,SB_PORT_OUT0
; * Dummy write 32+1
					MOVL	33
					ANDL	0XFF	; force Z=0
					CALL	DAP_TURN
DAP_SWD_TRAN_NO_E:	BS		SFR_PORT_IO,SB_PORT_OUT0
					JMP		PIOC_WAIT_HOST
;
; BREAKS
DAP_SWJ_SEQ:		NOP
					NOP
					JMP		PIOC_WAIT_HOST
;
;
;
;
PIOC_INIT:			NOP
					NOP
					MOVA2F	0B00000001	; IO0=1, IO1=0
					MOVA1F	0B00001111	; IO0(OPU), IO1(OPU)
PIOC_INIT_1:		WAITB	WB_DATA_MW_SR_1
; Load initial IO state
;					MOV		PIOC_CMD_CODE,A
;					MOVA	SFR_INDIR_ADDR
;					BP1F	BO_PORT_OUT0,2
;					BP1F	BO_PORT_OUT1,0
					MOV		DAP_CMD_TYPE,A
					XORL	0X01
					JZ		DAP_SWD_TRAN
					MOV		DAP_CMD_TYPE,A
					XORL	0X02
					JZ		DAP_SWJ_SEQ
					JMP		PIOC_INIT_1
;
PIOC_WAIT_HOST:		MOV		DAP_CMD_RESP,A
					MOVA	PIOC_CMD_RESP
;					MOVA1F	0B00001100	; IO0(IPU), IO1(IPU)
					WAITB	WB_DATA_SW_MR_0
					BC		SFR_SYS_CFG,SB_INT_REQ
					JMP		PIOC_INIT_1
;
WLP:				JMP		WLP
;
					ORG		0X0400
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
					DW		0X0000
;
END
