; ================
; DAP SWD_Transfer function, takes arguments
;  - [U8B]fastFlag in PIOC_CMD_CODE[7]
;  - [U8]request in DAP_CMD_ARGV
;  - [U32]data in DAP_DATA_D0-D3 (read/write)
; BREAKS: A, Z, C, SFR_DATA_EXCH, SFR_INDIR_ADDR
DAP_SWD_TRAN:		MOV		DAP_CMD_ARGV,A
					MOVA	SFR_DATA_EXCH
					MOVA1F	0B00001111			; IO0(OPU), IO1(OPU)
					CLR		SFR_INDIR_ADDR
					BTSC	PIOC_CMD_CODE,7
					CALL	DAP_PACKREQ_FAST
					BTSS	PIOC_CMD_CODE,7
					CALL 	DAP_PACKREQ
; - TURN
					MOVA1F	0B00001110			; IO0(IPU), IO1(OPU)
					MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
; - ACK Response
					CLR		SFR_DATA_EXCH

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,0
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,1
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2

					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					MOV		SFR_DATA_EXCH,A		; Save ACK in DAP_CMD_RESP
					MOVA	DAP_CMD_RESP
					CMPL	0X01				; - If DAP_OK(0x01)
					JZ		DAP_SWD_TRAN_OK
					CMPL	0x02				; - If DAP_WAIT(0x02)		
					JZ		DAP_SWD_TRAN_NO
					CMPL	0x04				; - If DAP_FAULT(0x04)
					JZ		DAP_SWD_TRAN_NO
					MOV		DAP_TURN_CYCLE,A	; - Else is error, TURN+33.
					CALL	DAP_TURN
					MOVL	33
					ANDL	0XFF				; force Z=0
					CALL	DAP_TURN
					BS		SFR_PORT_IO,SB_PORT_OUT0
					MOVA1F	0B00001111			; IO0(OPU), IO1(OPU)
					JMP		PIOC_WAIT_HOST
; - DAP_OK:
DAP_SWD_TRAN_OK:	BTSS	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_OK_W
; - Read data
					CLR		SFR_INDIR_ADDR
					BTSS	PIOC_CMD_CODE,7
					JMP		DAP_SWD_TOK_RN
					JMP		DAP_SWD_TOK_RF
DAP_SWD_TOK_RF:		CALL	DAP_READ8_FAST		; Read fast
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D0
					CALL	DAP_READ8_FAST
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D1
					CALL	DAP_READ8_FAST
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D2
					CALL	DAP_READ8_FAST
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D3
					JMP		DAP_SWD_TOK_RP				
DAP_SWD_TOK_RN:		CALL	DAP_READ8			; Read normal
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D0
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D1
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D2
					CALL	DAP_READ8
					MOV		SFR_DATA_EXCH,A
					MOVA	DAP_DATA_D3
DAP_SWD_TOK_RP:		CLR		SFR_DATA_EXCH		; - Read parity
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BG2F	BI_PORT_IN0,0
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					MOV		SFR_INDIR_ADDR,A	; - Check parity
					ANDL	0X01
					XOR		SFR_DATA_EXCH,F
					JZ		DAP_SWD_TRAN_OK_R1
					MOVL	0X08				; - Parity mismatch, return error code
					MOVA	DAP_CMD_RESP
DAP_SWD_TRAN_OK_R1:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111			; IO0(OPU), IO1(OPU)
					JMP		DAP_SWD_TRAN_OK_TS
; - Write data
DAP_SWD_TRAN_OK_W:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111			; IO0(OPU), IO1(OPU)
					CLR		SFR_INDIR_ADDR
					BTSS	PIOC_CMD_CODE,7
					JMP		DAP_SWD_TOK_WN
					JMP		DAP_SWD_TOK_WF
DAP_SWD_TOK_WF:		MOV		DAP_DATA_D0,A		; Write fast
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8_FAST
					MOV		DAP_DATA_D1,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8_FAST
					MOV		DAP_DATA_D2,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8_FAST
					MOV		DAP_DATA_D3,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8_FAST
					JMP		DAP_SWD_TOK_WP
DAP_SWD_TOK_WN:		MOV		DAP_DATA_D0,A		; Write normal
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D1,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D2,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
					MOV		DAP_DATA_D3,A
					MOVA	SFR_DATA_EXCH
					CALL	DAP_WRITE8
DAP_SWD_TOK_WP:		BP1F	BO_PORT_OUT0,0		; - Send parity
					BC		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
					BS		SFR_PORT_IO,SB_PORT_OUT1
					BTSS	PIOC_CMD_CODE,7
					CALL	DELAY_L2
; - Capture TS
DAP_SWD_TRAN_OK_TS:	BTSC	DAP_CMD_ARGV,7
					BS		SFR_SYS_CFG,SB_INT_REQ
; - Idle cycles
					MOV		DAP_IDLE_CYCLE,A
					JZ		DAP_SWD_TRAN_OK_E
					BC		SFR_PORT_IO,SB_PORT_OUT0
					CALL	DAP_TURN
DAP_SWD_TRAN_OK_E:	BS		SFR_PORT_IO,SB_PORT_OUT0
					JMP		PIOC_WAIT_HOST
;
; - DAP_WAIT/FAULT:
DAP_SWD_TRAN_NO:	MOV		DAP_DATA_PHASE,F
					JZ		DAP_SWD_TRAN_NO_1
					BTSS	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_NO_1
; * Dummy Read 32+1
					MOVL	33
					ANDL	0XFF				; force Z=0
					CALL	DAP_TURN
DAP_SWD_TRAN_NO_1:	MOV		DAP_TURN_CYCLE,A
					CALL	DAP_TURN
					MOVA1F	0B00001111			; IO0(OPU), IO1(OPU)
					MOV		DAP_DATA_PHASE,F
					JZ		DAP_SWD_TRAN_NO_E
					BTSC	DAP_CMD_ARGV,1
					JMP		DAP_SWD_TRAN_NO_E
					BC		SFR_PORT_IO,SB_PORT_OUT0
; * Dummy write 32+1
					MOVL	33
					ANDL	0XFF				; force Z=0
					CALL	DAP_TURN
DAP_SWD_TRAN_NO_E:	BS		SFR_PORT_IO,SB_PORT_OUT0
					JMP		PIOC_WAIT_HOST
;
; ================
; DAP SWD_Transfer function, not implemented
; BREAKS
DAP_SWJ_SEQ:		NOP
					NOP
					JMP		PIOC_WAIT_HOST
;
